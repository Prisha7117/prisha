//i. Monday to Sunday using switch case
//ii. Vowel or Consonant using switch case
#include<stdio.h>
main()
{
	
	printf("seven days of week are as follows :");
	int days;
	switch(days)
	{
	
	case 1:
		printf("MONDAY");
		break;
		case 2:
		printf("TUESDAY");
		break;
		case 3:
		printf("WEDNUSDAY");
		break;
		case 4:
		printf("THURSDAY");
		break;
		case 5:
		printf("FRIDAY");
		break;
		case 6:
		printf("SATURDAY");
		break;
		case 7:
		printf("SUNDAY");
		break;
	}
	
}
